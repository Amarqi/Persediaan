public class baranglbh {
    private String no_lebih;
    private String kd_barang;
    private String ket_lbh;
    private double jml_lbh;
    private double total_lbh;

    public String getNo_lebih() {
        return no_lebih;
    }

    public void setNo_lebih(String no_lebih) {
        this.no_lebih = no_lebih;
    }

    public String getKd_barang() {
        return kd_barang;
    }

    public void setKd_barang(String kd_barang) {
        this.kd_barang = kd_barang;
    }

    public String getKet_lbh() {
        return ket_lbh;
    }

    public void setKet_lbh(String ket_lbh) {
        this.ket_lbh = ket_lbh;
    }

    public double getJml_lbh() {
        return jml_lbh;
    }

    public void setJml_lbh(double jml_lbh) {
        this.jml_lbh = jml_lbh;
    }

    public double getTotal_lbh() {
        return total_lbh;
    }

    public void setTotal_lbh(double total_lbh) {
        this.total_lbh = total_lbh;
    }
    
    
}
