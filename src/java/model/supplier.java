package model;

/**
 *
 * @author AMARQI
 */
public class supplier {
    private String kode_supp;
    private String nama_supp;
    private String alamat;
    private String telp;

    public String getKode_supp() {
        return kode_supp;
    }

    public void setKode_supp(String kode_supp) {
        this.kode_supp = kode_supp;
    }

    public String getNama_supp() {
        return nama_supp;
    }

    public void setNama_supp(String nama_supp) {
        this.nama_supp = nama_supp;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getTelp() {
        return telp;
    }

    public void setTelp(String telp) {
        this.telp = telp;
    }
    
    
}
